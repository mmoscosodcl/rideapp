class Content {
  final String title;
  final String subtitle;
  final String date;
  final String description;
  final String imagePath;
  final bool like;
  final int comments;
  final int likes;
  final int people;

  Content({
    required this.title,
    required this.subtitle,
    required this.date,
    required this.description,
    required this.imagePath,
    required this.like,
    required this.comments,
    required this.likes,
    required this.people,
  });
}


